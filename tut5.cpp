#include <iostream>
using namespace std;
int main()
{
    int num1, num2;
    cout << "Enter the value of num1"<<endl;
    /*<< is called insertion operator*/
    cin>> num1;
    /*>> is called extraction operator*/
    cout <<"Enter the value of num2"<<endl;
    cin >> num2;
    cout <<" sum is" << num1+num2;
}